package beans;

/**
 *
 * @author teacher
 */
public class MyValue {
     private int value;
     public MyValue(){
         value=1;
     }
     public MyValue(int v){
         value=v;
     }

    /**
     * @return the value
     */
    public int getValue() {
        return value;
    }

    /**
     * @param value the value to set
     */
    public void setValue(int value) {
        this.value = value;
    }
}
